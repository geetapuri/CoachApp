package com.example.demo;

public class InvoiceHeader {
	
	private String presentCounter;
	private String invoiceDue;
	private String invoiceAmount;
	private String invoiceDate;
	private String kidID;
	private String invoiceID;
	
	public String getPresentCounter() {
		return presentCounter;
	}
	public void setPresentCounter(String presentCounter) {
		this.presentCounter = presentCounter;
	}
	public String getInvoiceDue() {
		return invoiceDue;
	}
	public void setInvoiceDue(String invoiceDue) {
		this.invoiceDue = invoiceDue;
	}
	public String getInvoiceAmount() {
		return invoiceAmount;
	}
	public void setInvoiceAmount(String invoiceAmount) {
		this.invoiceAmount = invoiceAmount;
	}
	public String getInvoiceDate() {
		return invoiceDate;
	}
	public void setInvoiceDate(String invoiceDate) {
		this.invoiceDate = invoiceDate;
	}
	public String getKidID() {
		return kidID;
	}
	public void setKidID(String kidID) {
		this.kidID = kidID;
	}
	public String getInvoiceID() {
		return invoiceID;
	}
	public void setInvoiceID(String invoiceID) {
		this.invoiceID = invoiceID;
	}
	
	
	

}
